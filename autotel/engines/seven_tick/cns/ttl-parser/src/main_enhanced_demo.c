// Progress tracking
static void show_progress_bar(size_t current, size_t total, const char* label) {
    if (total == 0) return;
    
    int width = 50;
    float progress = (float)current / total;
    int pos = (int)(width * progress);
    
    fprintf(stderr, "\\r%s [%.*s%*s] %3.1f%% (%zu/%zu)", 
            label, pos, "‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà‚ñà", 
            width - pos, "", progress * 100.0, current, total);
    
    if (current == total) {
        fprintf(stderr, "\\n");
    }
    fflush(stderr);
}

